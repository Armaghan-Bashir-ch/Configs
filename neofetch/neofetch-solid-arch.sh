#!/usr/bin/env bash

# Define ANSI color codes
bright_blue="\e[94m"
reset="\e[0m"

# Multi-line ASCII string with color applied
ascii="${bright_blue}\
                  ▟█▙
                 ▟███▙
                ▟█████▙
               ▟███████▙
              ▂▔▀▜██████▙
             ▟██▅▂▝▜█████▙
            ▟█████████████▙
           ▟███████████████▙
          ▟█████████████████▙
         ▟███████████████████▙
        ▟█████████▛▀▀▜████████▙
       ▟████████▛      ▜███████▙
      ▟█████████        ████████▙
     ▟██████████        █████▆▅▄▃▂
    ▟██████████▛        ▜█████████▙
   ▟██████▀▀▀              ▀▀██████▙
  ▟███▀▘                       ▝▀███▙
 ▟▛▀                               ▀▜▙${reset}"

# Run neofetch with colored ASCII
neofetch --ascii "$ascii" --ascii_distro off

